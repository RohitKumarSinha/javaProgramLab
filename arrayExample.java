public class arrayExample {  
    public static void main(String[] args) {  
        char [] arr = new char [] {'a', 'b', 'c', 'd'};  
        System.out.println("Elements of given array: ");  
        for (int i = 0; i < arr.length; i++) {  
            System.out.print(arr[i] + " ");  
        }  
    }  
}  

